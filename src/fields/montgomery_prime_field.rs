//! Static prime fields backed by `crypto-bigint` Montgomery arithmetic.
//!
//! [`Fp`] models a prime field `𝔽_p = ℤ/pℤ` whose modulus is known at
//! compile time through `crypto_bigint::const_prime_monty_params!`. Elements
//! are stored as [`ConstMontyForm`], so ordinary field operations reuse the
//! fixed-width Montgomery routines from `crypto-bigint` instead of the small
//! `u64` arithmetic used by the removed legacy backend.
//!
//! This backend is intentionally static: it is for large, known-at-compile-time
//! primes, not for runtime-selected prime fields.

